@app.route("/post/<int:post_id>", methods=["GET", "POST"])
def show_post(post_id):
    comment_form = CommentForm()
    requested_post = BlogPost.query.get(post_id)
    post_comments = Comment.query.filter_by(post_id=post_id)

    if request.method == "POST" and comment_form.validate_on_submit():
        if not current_user.is_authenticated:
            flash("You need to be logged on to write out comments.")
            return redirect(url_for("login"))

        new_comment = Comment(
            text=comment_form.body.data,
            comment_author=current_user,
            parent_post=requested_post,
        )
        db.session.add(new_comment)
        db.session.commit()
        return redirect(url_for("show_post", post_id=post_id))

    return render_template("post.html", post=requested_post, current_user=current_user, form=comment_form, comments=post_comments)


