@app.route('/register', methods=["GET", "POST"])
def register():
    new_user_form = RegisterForm()
    if request.method == "POST" and new_user_form.validate_on_submit():
        existing_email = new_user_form.email.data
        user = User.query.filter_by(email=existing_email).first()
        if user:
            flash("You've already signed up with that email, log in instead!")
            return redirect(url_for("login"))

        hash_pw =generate_password_hash(new_user_form.password.data, method="pbkdf2:sha256", salt_length=8)

        new_user = User(
            email=new_user_form.email.data,
            password=hash_pw,
            name=new_user_form.name.data,
        )
        db.session.add(new_user)
        db.session.commit()

        login_user(new_user)

        return redirect(url_for("get_all_posts"))

    return render_template("register.html", form=new_user_form, current_user=current_user)


@app.route('/login', methods=["GET", "POST"])
def login():
    if current_user.is_authenticated:
        return redirect(url_for("get_all_posts"))
    login_form = LoginForm()
    if request.method == "POST" and login_form.validate_on_submit():
        email = login_form.email.data
        password = login_form.password.data
        user = User.query.filter_by(email=email).first()

        if not user:
            flash("Email does not exist. Please try again or register with us.")
            return redirect(url_for("login"))

        if check_password_hash(user.password, password):
            login_user(user)
            return redirect(url_for("get_all_posts"))
        else:
            flash("Password incorrect. Please try again.")
            return redirect(url_for("login"))

    return render_template("login.html", form=login_form, current_user=current_user)


